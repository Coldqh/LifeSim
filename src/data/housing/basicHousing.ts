export function getHousingById(id: HousingId | undefined): Housing | undefined {
  if (!id) return undefined;
  return basicHousing.find((housing) => housing.id === id);
}

export function getHousingForLocation(locationIdValue: LocationId | undefined): Housing | undefined {
  if (!locationIdValue) return undefined;
  return basicHousing.find((housing) => housing.locationId === locationIdValue);
}
